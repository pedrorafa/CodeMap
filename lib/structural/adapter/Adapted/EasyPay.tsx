export class EasyPay {
  setValue(value: number): void {
    this.setValue(value);
  }
  setAmounts(amounts: number): void {
    this.setAmounts(amounts);
  }
  setCard(card: string): void {
    this.setCard(card);
  }
  setCVV(cvv: number): void {
    this.setCVV(cvv);
  }
  validateCard(): boolean {
    return this.validateCard();
  }
  pay(): boolean {
    return this.pay();
  }
}
